# Define a tuple with different data types
data = (42, 3.14, "Hello", True)

# Unpack the tuple into separate variables
integer_val, float_val, string_val, boolean_val = data

# Print each value and its corresponding type
print(f"Integer: {integer_val} (Type: {type(integer_val)})")
print(f"Float: {float_val} (Type: {type(float_val)})")
print(f"String: {string_val} (Type: {type(string_val)})")
print(f"Boolean: {boolean_val} (Type: {type(boolean_val)})")
